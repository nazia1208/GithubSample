package org.datatypes;

public class PrimitiveDataTypes {
	boolean a;
	byte b;
	char c;
	short d;
	int e;
	long f;
	float g;
	double h;
	char i;
	
	public static void main(String[] args)
	{
		PrimitiveDataTypes PDT=new PrimitiveDataTypes();
		
		System.out.println(PDT.a);
		System.out.println(PDT.b);
		System.out.println(PDT.c);
		System.out.println(PDT.d);
		System.out.println(PDT.e);
		System.out.println(PDT.f);
		System.out.println(PDT.g);
		System.out.println(PDT.h);
		System.out.println(PDT.i);
		}
	
}
